package edu.duke.oit.proconsulapi;

public class GroupHostMapping {
	private String groupurn;
	private String oudn;
	private String fqdn;
	public String getGroupurn() {
		return groupurn;
	}
	public void setGroupurn(String groupurn) {
		this.groupurn = groupurn;
	}
	public String getOudn() {
		return oudn;
	}
	public void setOudn(String oudn) {
		this.oudn = oudn;
	}
	public String getFqdn() {
		return fqdn;
	}
	public void setFqdn(String fqdn) {
		this.fqdn = fqdn;
	}
	
}
